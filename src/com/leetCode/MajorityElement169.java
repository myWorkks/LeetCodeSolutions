class Solution169 {
	public int majorityElement(int[] nums) {
		Set<Integer> distinct = new TreeSet<Integer>();
		List<Integer> list = new ArrayList<Integer>();
		for (int i : nums) {
			distinct.add(i);
			list.add(i);
		}
		int majorityElement = 0;
		Map<Integer, Long> map = new HashMap<Integer, Long>();
		System.out.println(distinct);
		System.out.println(list);
		List<Long> valueList = new ArrayList<Long>();
		for (Integer i : distinct) {
			long ll = list.stream().filter(l -> l .equals(i) ).count();
			map.put(i, ll);
			valueList.add(ll);
		}
		System.out.println(map);
		long maxValue = valueList.stream().sorted().max((x, y) -> x.compareTo(y)).get();
		Set<Map.Entry<Integer, Long>> kk = map.entrySet();

		for (Map.Entry<Integer, Long> entry : kk) {
			if (entry.getValue().equals(maxValue)) {
				majorityElement = entry.getKey();
				break;
			}

		}
		System.out.println(majorityElement);
		return majorityElement;
	}
}

